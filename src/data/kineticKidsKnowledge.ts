  // Search all categories and questions
  for (const categoryKey of Object.keys(kineticKidsKnowledge)) {
    const category = kineticKidsKnowledge[categoryKey];
    for (const item of category.questions) {
      // Check keywords
      const matched = item.keywords.some(kw => q.includes(kw.toLowerCase()));
      if (matched) {
        return { answer: item.answer, quickActions: item.quickActions };
      }
    }
  }

  // Fallback partial matching ‚Äî check if any word in query hits a keyword
  for (const categoryKey of Object.keys(kineticKidsKnowledge)) {
    const category = kineticKidsKnowledge[categoryKey];
    for (const item of category.questions) {
      const words = q.split(/\s+/).filter(w => w.length > 2);
      const partialMatch = words.some(word =>
        item.keywords.some(kw => kw.toLowerCase().includes(word) || word.includes(kw.toLowerCase()))
      );
      if (partialMatch) {
        return { answer: item.answer, quickActions: item.quickActions };
      }
    }
  }

